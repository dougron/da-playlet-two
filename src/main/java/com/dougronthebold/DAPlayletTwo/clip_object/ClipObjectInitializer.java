package main.java.com.dougronthebold.DAPlayletTwo.clip_object;

public class ClipObjectInitializer {
	
	public int clipObjectIndex;
	public int track;
	public int clip;
	public String name;

	public ClipObjectInitializer(int clipObjectIndex, int track, int clip, String name){
		this.clipObjectIndex = clipObjectIndex;
		this.track = track;
		this.clip = clip;
		this.name = name;
	}
}
